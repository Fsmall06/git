/* lcd_font_get_basic_5x7_glyph：获取基础 5x7 ASCII 点阵。
 *
 * 参数：
 *     ch：需要显示的 ASCII 字符。
 *
 * 返回：
 *     点阵数组地址；未知字符返回空格点阵，避免屏幕显示乱码。
 *
 * 调用方法：
 *     const uint8_t *glyph = lcd_font_get_basic_5x7_glyph('T');
 */
const uint8_t *lcd_font_get_basic_5x7_glyph(char ch)
{
    if ((ch >= '0') && (ch <= '9'))
    {
        return s_font5x7_digits[(size_t)(ch - '0')];
    }

    if ((ch >= 'A') && (ch <= 'Z'))
    {
        return s_font5x7_uppercase[(size_t)(ch - 'A')];
    }

    if ((ch >= 'a') && (ch <= 'z'))
    {
        return s_font5x7_uppercase[(size_t)(ch - 'a')];
    }

    switch (ch)
    {
        case ' ':
            return s_font5x7_space;
        case ':':
            return s_font5x7_colon;
        case '.':
            return s_font5x7_dot;
        case '-':
            return s_font5x7_minus;
        case '/':
            return s_font5x7_slash;
        case '%':
            return s_font5x7_percent;
        case '^':
            return s_font5x7_degree;
        case '?':
            return s_font5x7_question;
        default:
            return s_font5x7_space;
    }
}

